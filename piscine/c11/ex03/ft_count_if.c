int	ft_count_ft(char **tab, int length, int (*f)(char *))
{
	int	i;
	int	cnt;

	i = 0;
	cnt = 0;
	while (tab[i] != 0)
	{
		if (f(tab[i] != 0))
			cnt++;
		i++;
	}
	return (cnt);
}
