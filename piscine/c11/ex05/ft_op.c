int	ft_plus(int a, int b)
{
	return (a + b);
}

int	ft_minus(int a, int b)
{
	return (a - b);
}

int	ft_multi(int a, int b)
{
	return (a * b);
}

int	ft_div(int a, int b)
{
	return (a / b);
}

int	ft_modulo(int a, int b)
{
	return (a % b);
}
