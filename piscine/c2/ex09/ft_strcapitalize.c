char	*ft_strcapitalize(char *str)
{
	int	i;

	i = 1;
	if (str[0] >= 'a' && str[0] <= 'z')
		str[0] = str[0] - 32;
	while (str[i])
	{
		if (str[i - 1] < 48 || \
				(str[i - 1] >= 58 && str[i - 1] <= 64) || \
				(str[i - 1] >= 91 && str[i - 1] <= 96) || \
				str[i - 1] >= 123)
		{
			if (str[i] >= 'a' && str[i] <= 'z')
				str[i] = str[i] - 32;
			i++;
		}
		else
		{
			if (str[i] >= 'A' && str[i] <= 'Z')
				str[i] = str[i] + 32;
			i++;
		}
	}
	return (str);
}
